function scr_gettext(text)
    text = text or "Hello! This is testing text."

    if Mod.language == "english" then
        if Mod.TXT_ENGLISH[text] ~= nil then
            return Mod.TXT_ENGLISH[text]
        else
            return text
        end
    elseif Mod.language == "engrish" then
        if Mod.TXT_ENGRISH[text] ~= nil then
            return Mod.TXT_ENGRISH[text]
        else
            if Mod.TXT_ENGLISH[text] ~= nil then
                return Mod.TXT_ENGLISH[text]
            else
                return text
            end
        end
    elseif Mod.language == "spanish" then
        if Mod.TXT_SPANISH[text] ~= nil then
            return Mod.TXT_SPANISH[text]
        else
            return text
        end
    elseif Mod.language == "portuguese" then
        if Mod.TXT_PORTUGUESE[text] ~= nil then
            return Mod.TXT_PORTUGUESE[text]
        else
            return text
        end
    end
    return text
end
